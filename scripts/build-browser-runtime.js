const fs = require('node:fs')
const path = require('node:path')

const repoRoot = path.resolve(__dirname, '..')
const runtimeFiles = [
    path.join(repoRoot, 'server_v2', 'static', 'js', 'src', 'graph-stepper.js'),
    path.join(repoRoot, 'server_v2', 'static', 'js', 'src', 'graph-2.js'),
]

const outputDir = path.join(repoRoot, 'server_v2', 'static', 'js', 'dist')
const outputFile = path.join(outputDir, 'graph-runtime.js')

const readSource = function(filePath) {
    return fs.readFileSync(filePath, 'utf8').trim()
}

const buildBundle = function() {
    const joinedSources = runtimeFiles.map(readSource).join('\n\n')

    return [
        '/*',
        'Browser bundle for the v2 graph runtime.',
        'Generated by `npm run build`.',
        '*/',
        '(function(global) {',
        joinedSources,
        '',
        '    global.Stepper = Stepper',
        '    global.PipIndexStepper = PipIndexStepper',
        '    global.Graph = Graph',
        '    global.GraphRuntime = { Graph, Stepper, PipIndexStepper }',
        "})(typeof window !== 'undefined' ? window : globalThis)",
        '',
    ].join('\n')
}

fs.mkdirSync(outputDir, { recursive: true })
fs.writeFileSync(outputFile, buildBundle())

console.log(`Built ${path.relative(repoRoot, outputFile)}`)